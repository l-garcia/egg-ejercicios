

package Ejercicio6;


public class Cancion {
private String tituto,autor;

    public Cancion() {
    }

    public Cancion(String tituto, String autor) {
        this.tituto = tituto;
        this.autor = autor;
    }

    public String getTituto() {
        return tituto;
    }

    public void setTituto(String tituto) {
        this.tituto = tituto;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

  

}
